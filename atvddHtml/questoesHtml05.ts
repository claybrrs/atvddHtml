// 1. Quais são as principais tags usadas para criar tabelas em HTML? Explique a diferença entre as tags
// <th> e <td>, e quando você as usaria.

// R - Para criar tabelas, você usa as seguintes tags principais:
// <table> - Define o início e o fim de uma tabela.
// <tr> - Define uma linha na tabela.
// <th> - Define uma célula de cabeçalho em uma linha de tabela. O texto dentro de <th> geralmente é exibido em negrito e centralizado.
// <td> - Define uma célula de dados em uma linha de tabela. O texto dentro de <td> é exibido normalmente, alinhado à esquerda por padrão.

// <th> (Table Header Cell): Usado para criar células que servem como cabeçalhos para colunas ou linhas. Estas células são
// destacadas com negrito e centralizadas por padrão, ajudando a identificar o tipo de dados contidos em cada coluna ou linha.

// <td> (Table Data Cell): Usado para criar células que contêm os dados da tabela. Essas células são usadas para armazenar
// o conteúdo real da tabela, como números, textos, ou outros dados.

// 2. Embora tabelas sejam usadas para exibir dados tabulares, às vezes é recomendado evitá-las em
// favor de outras formas de apresentação de dados. Por quê? E quais são os cenários em que as
// tabelas são apropriadas?

// R -  Algumas razões para evitar o uso de tabelas:
// Tabelas podem ser difíceis de ler em dispositivos móveis ou em telas pequenas, onde o espaço é limitado.
// Gráficos e listas podem ser mais adequados para apresentação em tais contextos.
// Para dados complexos, gráficos e visualizações podem ajudar a destacar padrões e tendências de forma mais clara do que tabelas.
// Usuários com deficiências visuais podem encontrar dificuldades para interpretar tabelas, especialmente se não estiverem bem
// estruturadas para leitores de tela. Gráficos e resumos podem ser mais acessíveis.
// Gráficos, como gráficos de barras ou linhas, podem facilitar a comparação entre diferentes conjuntos de dados e fornecer
// contexto visual que uma tabela pode não oferecer.
// Em algumas situações, especialmente em layouts mais compactos, gráficos e listas podem ser mais eficientes em termos de espaço
// e design do que tabelas extensas.

// R - As tabelas são apropriadamente usadas quando você precisa comparar diferentes conjuntos de dados ou opções lado a lado,
// como características de produtos, preços, ou desempenhos de diferentes modelos.
// Para mostrar dados quantitativos, como estatísticas financeiras, resultados de pesquisas ou tabelas de frequências,
// onde uma estrutura de linhas e colunas facilita a leitura e análise.
// Em relatórios ou estudos, onde você resume grandes volumes de dados em uma forma compacta, permitindo uma visão rápida
// das informações mais relevantes.
// Quando você precisa listar informações organizadas em categorias, como uma lista de contatos, horários de eventos,
// ou itens de inventário.
// Para mostrar mudanças ao longo do tempo, como cronogramas, planos de projetos ou históricos de eventos.
// Quando você organiza dados em categorias ou classes, como classificações de produtos ou resultados de exames em diferentes categorias.
