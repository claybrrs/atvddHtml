// 1. Como a tag <form> é utilizada para criar formulários em HTML? Cite pelo menos três tipos
// diferentes de campos de entrada (<input>) que podem ser usados em um formulário.

// R - A tag <form> é usada para criar formulários que permitem aos usuários enviar dados para um servidor.
// <input type="text"> - Permite a entrada de uma linha de texto.
// <input type="password"> - Permite a entrada de texto que é oculto, geralmente utilizado para senhas.
// <input type="date"> - Permite a seleção de uma data através de um seletor de data.

// 2. Além dos campos de entrada, quais outros elementos podem ser incluídos em um formulário para
// melhorar a usabilidade e a experiência do usuário?

// <label> - Associam texto descritivo aos campos de entrada, tornando o formulário mais acessível e claro para os usuários.
// <select> - Oferecem uma lista de opções para o usuário escolher, economizando espaço e evitando erros de entrada.
// <input type="radio"> - Permitem que o usuário selecione uma única opção de um conjunto predefinido.
// <input type="checkbox"> - Permitem que o usuário selecione uma ou mais opções de um conjunto de opções.
// Placeholder - Texto temporário dentro de um campo de entrada que fornece uma dica sobre o que deve ser inserido no campo.
