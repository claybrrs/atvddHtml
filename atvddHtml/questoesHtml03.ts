// páginas, para arquivos locais e criar âncoras dentro de uma página.

// R - A tag <a> em HTML é usada para criar links, permitindo que você direcione os usuários para outras páginas da web, 
// arquivos locais ou âncoras dentro da mesma página. 

// R - Para criar um link para uma página da web externa, você deve usar o atributo href da tag <a>,
// especificando o URL da página de destino.

// R - Você também pode vincular a arquivos armazenados localmente no seu servidor ou no mesmo diretório onde está a página HTML. Por exemplo,
// para vincular a um arquivo PDF chamado "documento.pdf" que está na mesma pasta que a página html.


// 2. Além do atributo href, quais outros atributos importantes a tag <a> pode ter? Como o atributo
// target pode ser usado para controlar como o link é aberto pelo navegador?

// R - title: Fornece um texto de dica que aparece quando o usuário passa o mouse sobre o link.

// R - type: Especifica o tipo de conteúdo do link. É mais utilizado em elementos <link> do que em <a>, mas pode ser aplicado para 
// indicar o tipo MIME do recurso vinculado.

