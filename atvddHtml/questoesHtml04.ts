// 1. Descreva a diferença entre os atributos src e alt em relação à tag <img>. Por que é importante
// fornecer um atributo alt adequado para imagens?

// R - src (source): Especifica o caminho para a imagem que deve ser exibida. É o atributo que
// define de onde o navegador deve carregar a imagem. Sem ele, a imagem não será exibida.

// alt (alternative text): Fornece um texto alternativo que descreve a imagem. Esse texto é exibido
// se a imagem não puder ser carregada por algum motivo, como uma falha no carregamento ou um problema
//  com o link da imagem. Além disso, o texto alt é utilizado por leitores de tela para descrever a imagem
//   para pessoas com deficiência visual.

// A importância de fornecer um atributo alt adequado inclui:

//  Facilita a navegação para usuários com deficiências visuais, permitindo que eles compreendam o conteúdo visual da página.
// Melhora a otimização para mecanismos de busca, ajudando os motores de busca a entender o conteúdo da imagem.
// Oferece informações úteis quando a imagem não pode ser carregada, evitando uma experiência de usuário confusa ou incompleta.


