// 1. O que são elementos HTML5 semânticos e por que são importantes na estruturação de uma
// página? Cite três exemplos de elementos semânticos e explique quando você os usaria.

// R - Elementos html semânticos são tags que possuem um significado claro e específico sobre a função e o conteúdo que elas contêm.
// Diferente dos elementos genéricos, como <div> e <span>, que não fornecem informações sobre o tipo de conteúdo que encapsulam,
// os elementos semânticos ajudam a estruturar a página de forma que o conteúdo seja mais compreensível tanto para os navegadores
// quanto para os motores de busca e leitores de tela.

// R - <header> - Usado para definir o cabeçalho de uma seção ou de toda a página. Geralmente contém o logo,
// o título do site e a navegação principal.
// <article> -  Representa um conteúdo independente e auto-suficiente dentro de um documento, como um post de blog ou uma notícia.
// É ideal para qualquer bloco de conteúdo que possa ser distribuído e compreendido fora do contexto do restante da página. 
// <footer> - Usado para definir o rodapé de uma seção ou da página. Geralmente contém informações sobre direitos autorais,
// contatos e links importantes.

// 2. Além da semântica, como os elementos HTML5 podem afetar a acessibilidade das páginas web? Por
// que é fundamental considerar a acessibilidade ao escolher elementos de estruturação?

// R - Elementos semânticos ajudam a criar uma estrutura clara e lógica na página, o que é crucial para leitores de tela.
// Por exemplo, o uso de <header>, <nav>, <main>, <article>, e <footer> permite que usuários de leitores de tela compreendam
// a organização da página e naveguem facilmente entre diferentes seções.
// Também ajudam a criar uma navegação mais intuitiva. Por exemplo, o <nav> define áreas de navegação, facilitando para usuários de
// leitores de tela identificar e saltar diretamente para o menu de navegação. Além disso, elementos como <button>, <a>, e <form>
// têm comportamentos e características que permitem uma navegação mais previsível e acessível para usuários que dependem de teclado
// ou tecnologias assistivas.
// Tags como <figure>, <figcaption>, e <main> permitem que informações adicionais sejam associadas a imagens e seções principais,
// tornando o conteúdo mais descritivo e acessível. A inclusão de descrições apropriadas e contextos para elementos de mídia ajuda
// a garantir que todos os usuários, inclusive aqueles com deficiências visuais, possam entender o conteúdo.

// R - A acessibilidade garante que pessoas com diferentes tipos de deficiência (visual, auditiva, motora, cognitiva)
// possam acessar e interagir com o conteúdo web. Isso não só atende a requisitos legais e éticos, mas também amplia o público-alvo
// e melhora a experiência do usuário para todos.
// Um design acessível melhora a usabilidade geral da página para todos os usuários. Elementos bem estruturados e semanticamente
// corretos facilitam a navegação e a compreensão, o que é benéfico para todos, não apenas para pessoas com deficiências.
// Motores de busca favorecem páginas que são bem estruturadas e acessíveis, pois isso melhora a experiência do usuário.
// A semântica ajuda a organizar e apresentar o conteúdo de forma que seja facilmente compreendida e indexada.
// 







